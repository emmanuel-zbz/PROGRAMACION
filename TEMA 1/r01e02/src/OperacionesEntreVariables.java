
public class OperacionesEntreVariables {
  
  public static void main(String[] args) {
    
    int valor1 = 256;
    int valor2 = 398;
    int suma = valor1 + valor2;
    int diferencia = valor2 - valor1;
    int producto = valor1 * valor2;
    int cociente = valor2 / valor1;
    int modulo = valor2 % valor1;
      
    
    System.out.printf("La suma de los 2 valores es %d%n", suma);
    
    System.out.printf("La diferencia de los 2 valores es %d%n", diferencia);

    System.out.printf("El producto de los 2 valores es %d%n", producto);

    System.out.printf("El cociente de los 2 valores es %d%n", cociente);
    
    System.out.printf("El modulo de los 2 valores es %d%n", modulo);
  }

}
